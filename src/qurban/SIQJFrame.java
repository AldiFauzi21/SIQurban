    private String getLikeCondition(String query, boolean kondisi, String hewan){
        String hasil="";
        if(kondisi){
            if("".equals(query)){
                hasil+=" AND ";
            }else{
                hasil+=" OR ";
            }
            hasil+="kode LIKE '"+hewan+"%'";
        }
        return hasil;
    }
