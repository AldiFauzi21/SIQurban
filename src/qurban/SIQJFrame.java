    private void tambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahButtonActionPerformed
        String pengurbanValue = pengurban.getText(), jenisValue = jenis.getText(), zakarValue = zakar.getText(), umurValue = usia.getText(), bobotValue = bobot.getText(), count=getCountHewan();
        int zakarIntValue = Integer.parseInt(zakarValue), umurIntValue = Integer.parseInt(umurValue), bobotIntValue = Integer.parseInt(bobotValue);
        boolean sehatValue, butaValue, pincangValue, tandukValue, telingaValue, ekorValue, jantanValue;
        pengurbanValue=pengurbanValue.toLowerCase();
        jenisValue=jenisValue.toLowerCase();
        sehatValue = getCondition(sehat);
        butaValue = getCondition(buta);
        pincangValue = getCondition(pincang);
        tandukValue = getCondition(tanduk);
        telingaValue = getCondition(telinga);
        ekorValue = getCondition(ekor);
        jantanValue = getCondition(jantan);
        switch(jenisValue){
            case "kambing" : 
                Kambing kambing = new Kambing(jantanValue, zakarIntValue, umurIntValue, bobotIntValue, sehatValue, butaValue, pincangValue, tandukValue, telingaValue, ekorValue, pengurbanValue, count);
                tambahHewan(kambing);
                break;
            case "domba" : 
                Domba domba = new Domba(jantanValue, zakarIntValue, umurIntValue, bobotIntValue, sehatValue, butaValue, pincangValue, tandukValue, telingaValue, ekorValue, pengurbanValue, count);
                tambahHewan(domba);
                break;
            case "sapi" : 
                Sapi sapi = new Sapi(jantanValue, zakarIntValue, umurIntValue, bobotIntValue, sehatValue, butaValue, pincangValue, tandukValue, telingaValue, ekorValue, pengurbanValue, count);
                tambahHewan(sapi);
                break;
            case "kerbau" : 
                Kerbau kerbau = new Kerbau(jantanValue, zakarIntValue, umurIntValue, bobotIntValue, sehatValue, butaValue, pincangValue, tandukValue, telingaValue, ekorValue, pengurbanValue, count);
                tambahHewan(kerbau);
                break;
        }
    }//GEN-LAST:event_tambahButtonActionPerformed

