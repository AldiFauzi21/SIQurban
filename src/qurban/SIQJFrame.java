    private void tampilData(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("Nama");
        tabel.addColumn("Alamat");
        dataPenerima.setModel(tabel);
        try{
            res=stat.executeQuery("select nama, alamat from penerima");
            while (res.next()){
                tabel.addRow(new Object[]{
                    res.getString("nama"),
                    res.getString("alamat"),
                });
            }
        }catch(SQLException e){
            System.err.println("Tampilan gagal "+ e.getMessage());
        }
        DefaultTableModel tabel2 = new DefaultTableModel();
        tabel2.addColumn("Kode");
        tabel2.addColumn("Bobot");
        tabel2.addColumn("Sisa Kantong");
        dataHewan.setModel(tabel2);
        try{
            res=stat.executeQuery("select kode, bobot, kantong from hewan");
            while (res.next()){
                tabel2.addRow(new Object[]{
                    res.getString("kode"),
                    res.getString("bobot")+" kilogram",
                    res.getString("kantong")+" bungkus",
                });
            }
        }catch(SQLException e){
            System.err.println("Tampilan gagal "+ e.getMessage());
        }
    }
    public void cetak(String data){
        FileOutputStream foutput=null;
        //membuka file
        try{
            foutput = new FileOutputStream("d:/kupon.txt");
        }catch(IOException e){}
        //menulis data kedalam file
        try{
            for(int i=0;i<data.length();i++){
                foutput.write((int) data.charAt(i));
            }
        }catch(IOException e){}
        try{
            foutput.close();
        }catch(IOException e){}
    }
